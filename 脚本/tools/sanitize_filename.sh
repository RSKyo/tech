#!/usr/bin/env bash
IFS=$'\n\t'
set -Eeuo pipefail
trap 'echo "[ERROR] line $LINENO: $BASH_COMMAND" >&2' ERR

# =============================================================================
# sanitize_filename.sh
#
# 对输入字符串进行“文件名安全化 + 语义映射替换”
#
# 设计原则：
#   - 规则即数据（先定义规则，再统一遍历应用）
#   - 不做截断、不改大小写、不删除合法字符
#   - 仅处理跨平台不安全字符
#
# 用法：
#   sanitize_filename.sh "<string>"
# =============================================================================

# ------------------------------
# 参数检查
# ------------------------------
[ $# -ge 1 ] || {
  echo "Usage: $(basename "$0") <string>" >&2
  exit 1
}

input="$*"

# ------------------------------
# 规则定义区（核心）
#
# SANITIZE_RULES 约定：
#   每 3 项为一组：
#     [原字符] [替换为] [说明]
# ------------------------------
SANITIZE_RULES=(
  "/"  " - "  "路径分隔符"
  "\\" " - "  "Windows 路径分隔符"
  ":"  " - "  "标题分隔符"
  "|"  " - "  "管道符"

  "*"  "_"    "非法通配符"
  "?"  "_"    "非法问号"
  "\"" "'"    "双引号 → 单引号"

  "<"  "("    "左尖括号"
  ">"  ")"    "右尖括号"
)

# ------------------------------
# 预处理：去除首尾空白
# ------------------------------
s="${input#"${input%%[![:space:]]*}"}"
s="${s%"${s##*[![:space:]]}"}"

# ------------------------------
# 核心逻辑：按规则表遍历替换
# ------------------------------
for ((i = 0; i < ${#SANITIZE_RULES[@]}; i += 3)); do
  from="${SANITIZE_RULES[i]}"
  to="${SANITIZE_RULES[i+1]}"
  s="${s//"$from"/"$to"}"
done

# ------------------------------
# 后处理：压缩多余空格
# ------------------------------
s="$(printf '%s\n' "$s" | tr -s ' ')"

# ------------------------------
# 输出
# ------------------------------
printf '%s\n' "$s"
