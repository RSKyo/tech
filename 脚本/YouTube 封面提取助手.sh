#!/usr/bin/env bash
IFS=$'\n\t'
set -Eeuo pipefail
trap 'echo "[ERROR] line $LINENO: $BASH_COMMAND" >&2' ERR

# =============================================================================
# YouTube 封面提取助手.sh
#
# 功能定位：
#   YouTube 封面批量提取的入口脚本（调度器）
#
# 参数规则：
#   - 接收与 yt_thumbnail.sh 完全一致的参数
#   - 除 --out 外，所有参数原样传递给 yt_thumbnail.sh
#   - 若用户未显式传递 --out：
#       默认使用：本脚本所在目录下的 thumbnail/
#     这样可以避免 yt_thumbnail.sh 在其自身目录中
#     自动创建 thumbnail/ 目录
# =============================================================================

# -----------------------------------------------------------------------------
# 路径定位（本脚本位于 ROOT）
# -----------------------------------------------------------------------------
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" >/dev/null && pwd)"
ROOT_DIR="$SCRIPT_DIR"

# -----------------------------------------------------------------------------
# 通过 SHID 注入依赖命令
# -----------------------------------------------------------------------------
source "$ROOT_DIR/source/resolve_deps.source.sh"

resolve_deps \
  ROOT_DIR="$ROOT_DIR" \
  YT_URLS_CMD=Yjd7EHuw \
  YT_THUMB_CMD=R0BCnzp6

# -----------------------------------------------------------------------------
# usage
# -----------------------------------------------------------------------------
usage() {
  local cmd
  cmd="$(basename "$0")"

  cat >&2 <<EOF
用法：
  $cmd <input> [yt_thumbnail.sh 的参数...]

说明：
  <input>：
    - 单个 YouTube URL
    - URL 列表文件（.txt）
    - 单个已下载的视频文件
    - 指定目录（仅当前层）

参数传递规则：
  - 除 --out 外，其余参数将原样传递给 yt_thumbnail.sh
  - 若未指定 --out，则默认使用：
      $SCRIPT_DIR/thumbnail/
EOF
}

# -----------------------------------------------------------------------------
# 参数处理
# -----------------------------------------------------------------------------
[[ $# -ge 1 ]] || { usage; exit 1; }

INPUT="$1"
shift

# 剩余参数：视为 yt_thumbnail.sh 的参数
THUMB_ARGS=( "$@" )

# -----------------------------------------------------------------------------
# --out 兜底逻辑（唯一的参数干预点）
# -----------------------------------------------------------------------------
# 目的：
#   若不指定 --out，避免 yt_thumbnail.sh
#   在其自身目录下创建 thumbnail/
HAS_OUT=0
for arg in "${THUMB_ARGS[@]}"; do
  if [[ "$arg" == "--out" ]]; then
    HAS_OUT=1
    break
  fi
done

if [[ $HAS_OUT -eq 0 ]]; then
  THUMB_ARGS=( --out "$SCRIPT_DIR/thumbnail" "${THUMB_ARGS[@]}" )
fi

# -----------------------------------------------------------------------------
# 解析并收集 YouTube URL 列表
# -----------------------------------------------------------------------------
mapfile -t URLS < <("$YT_URLS_CMD" "$INPUT")

total=${#URLS[@]}
if [[ $total -eq 0 ]]; then
  echo "[WARN] 未解析到任何有效的 YouTube URL" >&2
  exit 0
fi

# -----------------------------------------------------------------------------
# 主处理循环（带进度提示）
# -----------------------------------------------------------------------------
idx=0
for url in "${URLS[@]}"; do
  idx=$((idx + 1))
  echo "[$idx/$total] $url" >&2
  "$YT_THUMB_CMD" "$url" "${THUMB_ARGS[@]}"
done
