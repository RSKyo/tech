#!/usr/bin/env bash

IFS=$'\n\t'
set -Eeuo pipefail
trap 'echo "[ERROR] line $LINENO: $BASH_COMMAND" >&2' ERR

# =============================================================================
# YouTube 曲目列表提取助手.sh
#
# 功能定位：
#   YouTube 在线音乐 Tracklist 批量提取的入口脚本（调度器）
#
# 设计原则：
#   - 只做“输入解析 + 参数转发 + 进度提示”
#   - Tracklist 解析逻辑全部由 yt_tracklist.sh 负责
#   - 行为与「YouTube 封面提取助手.sh」保持一致
#
# 输入 <input>：
#   - 单个 YouTube URL
#   - URL 列表文件（.txt）
#   - 单个已下载的视频文件
#   - 指定目录（仅当前层）
#
# 参数规则：
#   - 除 --out 外，其余参数将原样传递给 yt_tracklist.sh
#   - 若未指定 --out：
#       默认使用：本脚本所在目录下的 tracklist/
# =============================================================================

# -----------------------------------------------------------------------------
# 路径定位（本脚本位于 ROOT）
# -----------------------------------------------------------------------------
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" >/dev/null && pwd)"
ROOT_DIR="$SCRIPT_DIR"

# -----------------------------------------------------------------------------
# 通过 SHID 注入依赖命令
# -----------------------------------------------------------------------------
source "$ROOT_DIR/source/resolve_deps.source.sh"

resolve_deps \
  ROOT_DIR="$ROOT_DIR" \
  YT_URLS_CMD=Yjd7EHuw \
  YT_TRACKLIST_CMD=71Dp6FlP

# -----------------------------------------------------------------------------
# usage
# -----------------------------------------------------------------------------
usage() {
  local cmd
  cmd="$(basename "$0")"

  cat >&2 <<EOF
用法：
  $cmd <input> [yt_tracklist.sh 的参数...]

说明：
  <input>：
    - 单个 YouTube URL
    - URL 列表文件（.txt)
    - 单个已下载的视频文件
    - 指定目录（仅当前层）

参数传递规则：
  - 除 --out 外，其余参数将原样传递给 yt_tracklist.sh
  - 若未指定 --out，则默认使用：
      $SCRIPT_DIR/tracklist/
EOF
}

# -----------------------------------------------------------------------------
# 参数处理
# -----------------------------------------------------------------------------
[[ $# -ge 1 ]] || { usage; exit 1; }

INPUT="$1"
shift

# 剩余参数：转发给 yt_tracklist.sh
TRACKLIST_ARGS=( "$@" )

# -----------------------------------------------------------------------------
# --out 兜底逻辑（唯一的参数干预点）
# -----------------------------------------------------------------------------
HAS_OUT=0
for arg in "${TRACKLIST_ARGS[@]}"; do
  if [[ "$arg" == "--out" ]]; then
    HAS_OUT=1
    break
  fi
done

if [[ $HAS_OUT -eq 0 ]]; then
  TRACKLIST_ARGS=( --out "$SCRIPT_DIR/tracklist" "${TRACKLIST_ARGS[@]}" )
fi

# -----------------------------------------------------------------------------
# 解析并收集 YouTube URL 列表
# -----------------------------------------------------------------------------
mapfile -t URLS < <("$YT_URLS_CMD" "$INPUT")

total=${#URLS[@]}
if [[ $total -eq 0 ]]; then
  echo "[WARN] 未解析到任何有效的 YouTube URL" >&2
  exit 0
fi

# -----------------------------------------------------------------------------
# 主处理循环（带进度提示）
# -----------------------------------------------------------------------------
idx=0
for url in "${URLS[@]}"; do
  idx=$((idx + 1))
  echo "[$idx/$total] $url" >&2
  "$YT_TRACKLIST_CMD" "$url" "${TRACKLIST_ARGS[@]}"
done
