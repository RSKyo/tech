#!/usr/bin/env bash
# ==============================================================================
# yt_tracklist_extractor.sh
# ==============================================================================
#
# 从 YouTube 视频描述中提取 tracklist（仅基于时间戳存在性）。
#
# 支持时间格式：
#   - mm:ss
#   - h:mm:ss
#   - hh:mm:ss
#
# 行内匹配：
#   只要一行中出现合法时间戳，即视为 track 行
#
# ==============================================================================

set -euo pipefail

# ------------------------------------------------------------------------------
# Defaults
# ------------------------------------------------------------------------------
MIN_TRACK_LINES=2
ENABLE_LAST_LINE_LOOP=1

# ------------------------------------------------------------------------------
# CLI args
# ------------------------------------------------------------------------------
URL=""

while [[ $# -gt 0 ]]; do
  case "$1" in
    --min-lines)
      shift
      [[ "${1:-}" =~ ^[0-9]+$ ]] || {
        echo "错误：--min-lines 需要一个非负整数" >&2
        exit 1
      }
      MIN_TRACK_LINES="$1"
      ;;
    --loop-last)
      ENABLE_LAST_LINE_LOOP=1
      ;;
    --no-loop-last)
      ENABLE_LAST_LINE_LOOP=0
      ;;
    -h|--help)
      cat <<'EOF'
用法：
  yt_tracklist_extractor.sh [options] "<YouTube URL>"

选项：
  --min-lines N        最少需要的 track 行数（默认 2）
  --loop-last          最后一行视为 loop（默认）
  --no-loop-last       不处理最后一行 loop
EOF
      exit 0
      ;;
    -*)
      echo "未知参数：$1" >&2
      exit 1
      ;;
    *)
      URL="$1"
      ;;
  esac
  shift
done

[[ -n "$URL" ]] || {
  echo "错误：必须提供 YouTube URL" >&2
  exit 1
}

# ------------------------------------------------------------------------------
# Dependencies
# ------------------------------------------------------------------------------
command -v yt-dlp >/dev/null 2>&1 || {
  echo "❌ 缺少 yt-dlp，请先安装：brew install yt-dlp" >&2
  exit 1
}

# ------------------------------------------------------------------------------
# Fetch description
# ------------------------------------------------------------------------------
desc="$(yt-dlp --no-playlist -O "%(description)s" "$URL" 2>/dev/null || true)"
[[ -n "$desc" ]] || exit 0

# ------------------------------------------------------------------------------
# Time regex (single source of truth)
# ------------------------------------------------------------------------------
TIME_REGEX='([0-9]{1,2}:[0-9]{2}:[0-9]{2}|[0-9]{1,2}:[0-9]{2})'

# ------------------------------------------------------------------------------
# Collect track lines
# ------------------------------------------------------------------------------
tracks=()

while IFS='' read -r line || [[ -n "$line" ]]; do
  line="${line%$'\r'}"
  if [[ "$line" =~ $TIME_REGEX ]]; then
    tracks+=("$line")
  fi
done <<< "$desc"

# ------------------------------------------------------------------------------
# Guard
# ------------------------------------------------------------------------------
(( ${#tracks[@]} >= MIN_TRACK_LINES )) || exit 0

# ------------------------------------------------------------------------------
# Loop handling
# ------------------------------------------------------------------------------
if (( ENABLE_LAST_LINE_LOOP == 1 )); then
  last_idx=$((${#tracks[@]} - 1))
  [[ "${tracks[$last_idx]}" =~ $TIME_REGEX ]] &&
    tracks[$last_idx]="${BASH_REMATCH[1]} @loop"
fi

# ------------------------------------------------------------------------------
# Output
# ------------------------------------------------------------------------------
printf '%s\n' "${tracks[@]}"
